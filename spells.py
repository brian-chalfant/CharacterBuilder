def get_spells(user_class, level):
    rtn_list = []
    conn = sqlite3.connect('spells.db',)
    c = conn.cursor()
    c.execute("SELECT NAME FROM Spells WHERE {usc}_SPELL = 1 and LEVEL = {lvl} ORDER BY NAME ASC".format(usc=user_class, lvl=level))
    spell_list = c.fetchall()
    for row in spell_list:
        rtn_list.append(row)
    return rtn_list


